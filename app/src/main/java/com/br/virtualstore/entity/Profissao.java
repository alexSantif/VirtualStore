package com.br.virtualstore.entity;

/**
 * @author Alex
 *
 */
public class Profissao {
	
	private int codProfissao;
	private String descricao;
	private String subDescricao;
	private String urlImg;
	/**
	 * @return the codProfissao
	 */
	public int getCodProfissao() {
		return codProfissao;
	}
	/**
	 * @param codProfissao the codProfissao to set
	 */
	public void setCodProfissao(int codProfissao) {
		this.codProfissao = codProfissao;
	}
	/**
	 * @return the descricao
	 */
	public String getDescricao() {
		return descricao;
	}
	/**
	 * @param descricao the descricao to set
	 */
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	/**
	 * @return the subDescricao
	 */
	public String getSubDescricao() {
		return subDescricao;
	}
	/**
	 * @param subDescricao the subDescricao to set
	 */
	public void setSubDescricao(String subDescricao) {
		this.subDescricao = subDescricao;
	}
	/**
	 * @return the urlImg
	 */
	public String getUrlImg() {
		return urlImg;
	}
	/**
	 * @param urlImg the urlImg to set
	 */
	public void setUrlImg(String urlImg) {
		this.urlImg = urlImg;
	}
	
	

}
